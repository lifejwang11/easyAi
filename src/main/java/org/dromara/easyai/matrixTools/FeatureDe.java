package org.dromara.easyai.matrixTools;

public class FeatureDe {
    private Matrix featureValue;//特征值矩阵
    private Matrix featureMatrix;//特征向量矩阵

    public Matrix getFeatureValue() {
        return featureValue;
    }

    public void setFeatureValue(Matrix featureValue) {
        this.featureValue = featureValue;
    }

    public Matrix getFeatureMatrix() {
        return featureMatrix;
    }

    public void setFeatureMatrix(Matrix featureMatrix) {
        this.featureMatrix = featureMatrix;
    }
}
